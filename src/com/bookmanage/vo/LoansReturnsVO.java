package com.bookmanage.vo;

public class LoansReturnsVO {
	private String loansNum;
	private String returnsNum;
	private String userId;
	private String bookIsbn;
	private String loansDate;
	private String returnsDate;
	
	
	public String getLoansNum() {
		return loansNum;
	}
	public void setLoansNum(String loansNum) {
		this.loansNum = loansNum;
	}
	public String getReturnsNum() {
		return returnsNum;
	}
	public void setReturnsNum(String returnsNum) {
		this.returnsNum = returnsNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getBookIsbn() {
		return bookIsbn;
	}
	public void setBookIsbn(String bookIsbn) {
		this.bookIsbn = bookIsbn;
	}
	public String getLoansDate() {
		return loansDate;
	}
	public void setLoansDate(String loansDate) {
		this.loansDate = loansDate;
	}
	public String getReturnsDate() {
		return returnsDate;
	}
	public void setReturnsDate(String returnsDate) {
		this.returnsDate = returnsDate;
	}
	
	
}
